
public class Event {
	private int month;
	private int day;
	private int hora;
	private int intervalo;
	
	public Event(int month, int day, int hora, int intervalo) {
		super();
		this.month = month;
		this.day = day;
		this.hora = hora;
		this.intervalo = intervalo;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getHora() {
		return hora;
	}

	public void setHora(int hora) {
		this.hora = hora;
	}

	public int getIntervalo() {
		return intervalo;
	}

	public void setIntervalo(int intervalo) {
		this.intervalo = intervalo;
	}
	
	
	
	
	
	

}
